/// Represents the current vertical drag direction of a bottom sheet.
///
/// - `up`: The sheet is being dragged upward.
/// - `down`: The sheet is being dragged downward.
/// - `none`: No vertical drag is occurring.
public enum SheetDragDirection {
    case up
    case down
    case none
}
